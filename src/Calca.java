public class Calca {
    public static int b = 0;
    public static int calca (int q, int r, String WwW) {
        if (WwW.equals("+")) return q+r;
        if (WwW.equals("-")) return q-r;
        if (WwW.equals("*")) return q*r;
        if (WwW.equals("/")) return q/r;
        else return b;
    }
}
